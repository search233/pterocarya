ll mod(ll num) {
    num %= MOD;
    if (num < 0) num += MOD;
    return num;
}

void solve() {
    string s; cin >> s;
    int n; n = s.length();

    vector<int> lps(n + 5);
    vector<ll> cnt(n + 5);

    for (int i = 1, j = 0; i < n; ++i) {
        while (j && s[i] != s[j]) j = lps[j - 1];
        if (s[i] == s[j]) ++j;
        lps[i] = j;
        cnt[i] = 1 + cnt[j];
        cout << cnt[i] << " \n"[i == n - 1];
    }

    ll ans = 1;
    for (int i = 1, j = 0; i < n; ++i) {
        while (j && s[i] != s[j]) j = lps[j - 1];
        if (s[i] == s[j]) ++j;

        while (j && j > (i + 1) / 2) j = lps[j - 1];

        ans = mod(ans * (cnt[j] + 1));
    }

    cout << ans << '\n';
}

