void solve() {
    string s1, s2;
    cin >> s1 >> s2;

    int n = s1.length();
    int m = s2.length();

    vector<int> border(m);

    for (int l = 0, r = 1; r <= m; ++r) {
        while (l && s2[l] != s2[r]) l = border[l - 1];
        if (s2[l] == s2[r]) ++l;
        border[r] = l;
    }

    for (int i = 0, j = 0; i < n; ++i) {
        while (j && s1[i] != s2[j]) j = border[j - 1];
        if (s1[i] == s2[j]) ++j;
        if (j == m) {
            cout << i - m + 1 << '\n';
        }
    }

    for (auto i : border) {
        cout << i << ' ';
    }
}

