int dp[1 << 20][20] = {};

void solve() {
    int n; cin >> n;
    int dis[21][21];

    memset(dp, 0x3f, sizeof(dp));
    dp[1][0] = 0;
    
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            cin >> dis[i][j];
        }
    }

    // s -> s | (1 << j)
    // dp[s | (1 << j)][j] = min(dp[s | (1 << j)][j], dp[s][i] + dis[i][j]);
    for (int s = 1; s < (1 << n); s += 2) {
        for (int j = 1; j < n; ++j) {
            if (s & (1 << j)) continue;

            for (int i = 0; i < n; ++i) {
                if (s & (1 << i))
                dp[s | (1 << j)][j] = min(dp[s | (1 << j)][j], dp[s][i] + dis[i][j]);
            }
        }
    }


    int ans = 1e9;
    for (int i = 1; i < n; ++i) {
        ans = min(ans, dp[(1 << n) - 1][i] + dis[i][0]);
    }

    cout << ans << '\n';
}

