void solve() {
    ll x, d;
    cin >> x >> d;

    vector<ll> ans;
    
    ll cur = 1;
    // cout << __lg(x) << ' ' << __builtin_popcount(x);
    while (__lg(x) + 1 != __builtin_popcount(x)) {
        ll tmp = (1 << __lg(x)) - 1;
        x -= tmp;

        for (int i = 0; i < __builtin_popcount(tmp); ++i) {
            ans.push_back(cur);
        }

        cur += d;
    }

    for (int i = 0; i < __builtin_popcount(x); ++i) {
        ans.push_back(cur);
    }

    int len = ans.size();
    cout << len << '\n';
    for (int i = 0; i < len; ++i) {
        cout << ans[i] << " \n"[i == len - 1];
    }
}

