void solve() {
    int n; cin >> n;

    set<int> st;
    int g = 0;
    int mx = 0;

    for (int i = 1; i <= n; ++i) {
        int num; cin >> num;
        mx = max(mx, num);
        g = gcd(g, num);
        st.insert(num);
    }

    if ((mx / g - st.size()) & 1) {
        cout << "Alice\n";
    }
    else {
        cout << "Bob\n";
    }
}

