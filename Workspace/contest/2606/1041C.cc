void solve() {
    int n, m, d;
    cin >> n >> m >> d;

    multiset<int> st;
    map<int, vector<int>> id;

    for (int i = 1; i <= n; ++i) {
        int num; cin >> num;
        st.insert(num);
        id[num].push_back(i);
    }

    int t = 0;
    int ai = m;
    vector<int> ans(n + 1);

    for (int i = 1; i <= n; ++i) {
        auto it = st.upper_bound(ai + d);

        if (it == st.end()) {
            ++t;
            it = st.begin();
        }

        int num = *it;
        
        ai = num;
        ans[id[num].back()] = t;
        id[num].pop_back();

        st.erase(it);
    }

    cout << t << '\n';
    for (int i = 1; i <= n; ++i) {
        cout << ans[i] << " \n"[i == n];
    }
}

