class DSU {
private:
    int n;
    vector<ll> fa, sz;

    void init(int n) {
        this->n = n;
        fa.resize(n + 1);
        sz.resize(n + 1, 1);
        for (int i = 0; i <= n; i++) {
            fa[i] = i;
        }
    }

public:
    DSU(int n) {
        init(n);
    }

    int find(int x) {
        if (fa[x] != x)
            fa[x] = find(fa[x]);
        return fa[x];
    }

    bool same(int u, int v) {
        return find(u) == find(v);
    }

    void merge(int u, int v) {
        u = find(u);
        v = find(v);
        if (u == v) return;

        if (sz[u] < sz[v]) swap(u, v);

        fa[v] = u;
        sz[u] += sz[v];
    }

    ll size(int x) {
        return sz[find(x)];
    }
};

void solve() {
    int n, m;
    cin >> n >> m;

    vector<arr3> e;
    for (int i = 1; i < n; ++i) {
        int u, v, w;
        cin >> u >> v >> w;

        e.push_back({w, u, v});
    }

    ranges::sort(e);

    
    DSU dsu(n);
    map<int, ll> mp;
    for (auto [w, u, v] : e) {
        mp[w] += 1ll * dsu.size(u) * dsu.size(v);
        dsu.merge(u, v);
    }

    ll sum = 0;
    mp[0] = 0;
    for (auto& [w, cnt] : mp) {
        sum += cnt;
        cnt = sum;
    }

    for (int i = 0; i < m; ++i) {
        int q; cin >> q;
        auto it = mp.upper_bound(q);
        --it;
        cout << it->second << " \n"[i == m - 1];
    }
}

