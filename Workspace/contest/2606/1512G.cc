const int N = (int)1e7 + 1;

int num[N + 5];
int nnum[N + 5];

void init() {
    for (int i = 1; i < N; i++) {
        for (int j = i; j < N; j += i) {
            num[j] += i;
        }
    }

    memset(nnum, -1, sizeof(nnum));
    for (int i = 1; i <= N; ++i) {
        int tmp = num[i];
        if (tmp && tmp < N && nnum[tmp] == -1) {
            nnum[tmp] = i;
        }    
    }
}

void solve() {
    int c; cin >> c;
    cout << nnum[c] << '\n';
}

