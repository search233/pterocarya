void solve() {
    int n; cin >> n;
    int h, m, s;

    auto cal = [](int h, int m, int s) -> int {
        return h * 3600 + m * 60 + s;
    };

    vector<int> a(cal(23, 59, 59) + 2);
    for (int i = 0; i < n; ++i) {
        scanf("%d:%d:%d", &h, &m, &s);
        // cout << h << ' ' << m << ' ' << s << ' ';
        ++a[cal(h, m, s)];
        scanf("%d:%d:%d", &h, &m, &s);
        --a[cal(h, m, s) + 1];
        // cout << h << ' ' << m << ' ' << s << '\n';
    }

    int ans = a[0];
    for (int i = 1; i < a.size(); ++i) {
        // if (a[i])
        // cout << i << ' ' <<  a[i] << '\n';
        a[i] += a[i - 1];
        ans = max(ans, a[i]);
    }

    cout << ans;
} 

