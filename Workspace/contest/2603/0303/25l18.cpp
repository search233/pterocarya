using arr3 = array<int, 3>;
const double PI = acos(-1.0);

void solve() {
    int n, m, k;
    cin >> n >> m >> k;
    vector<arr3> a;
    vector b(n + 1, vector<int>(m + 1));

    for (int i = 1; i <= n; ++i) {
        for (int j = 1; j <= m; ++j) {
            cin >> b[i][j];
            a.push_back({b[i][j], i, j});
        }
    }

    ranges::sort(a, []
    (arr3 x, arr3 y) -> bool {
        return x[0] > y[0];
    });

    int cnt = 0;
    set<int> stx, sty;
    for (auto [num, x, y] : a) {
        if(stx.count(x) || sty.count(y)) continue;
        stx.insert(x), sty.insert(y);
        ++cnt;
        if (cnt == k) break; 
    }

    for (int i = 1; i <= n; ++i) {
        if (stx.count(i)) continue;
        int tag = 1;
        for (int j = 1; j <= m; ++j) {
            if (sty.count(j)) continue;
            if (tag) {
                tag = 0;
                cout << b[i][j];
            }
            else {
                cout << ' ' << b[i][j];
            } 
        }
        cout << '\n';
    }
} 

