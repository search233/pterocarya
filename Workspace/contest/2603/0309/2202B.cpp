void solve() {
    int n; cin >> n;
    string s; cin >> s;

    if ((n & 1) && s[0] == 'b') {
        cout << "NO\n";
        return;
    }

    for (int i = (n & 1); i < n; i += 2) {
        if (s[i] == s[i + 1] && s[i] != '?') {
            cout << "NO\n";
            return;
        }

        if (s[i] == '?' && s[i + 1] == '?') continue;
        else if (s[i] == '?') {
            s[i] = ((s[i + 1] - 'a') ^ 1) + 'a';
        }
        else {
            s[i + 1] = ((s[i] - 'a') ^ 1) + 'a';
        }
    }

    cout << "YES\n";
} 

