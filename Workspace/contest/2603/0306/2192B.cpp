const double PI = acos(-1.0);

void solve() {
    int n; cin >> n;
    string s; cin >> s;

    vector p(2, vector<int>());    
    for (int i = 0; i < n; ++i) {
        p[s[i] - '0'].push_back(i + 1);
    }

    auto print = [&](int num) -> void {
        int len = p[num].size();
        cout << len << '\n';
        for (int i = 0; i < len; ++i) {
            cout << p[num][i] << " \n"[i == len - 1];
        }
    };

    if (p[1].size() % 2 == 0) {
        print(1);
        return;
    }

    if (p[0].size() & 1) {
        print(0);
        return;
    }
    
    cout << "-1\n";
} 

