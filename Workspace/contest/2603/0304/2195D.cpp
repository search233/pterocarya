void solve() {
    int n; cin >> n;
    vector<ll> f(n + 1);
    vector<ll> a(n + 1);
    for (int i = 1; i <= n; ++i) {
        cin >> f[i];
    }

    for (int i = 2; i < n; ++i) {
        a[i] = (f[i + 1] + f[i - 1] - 2 * f[i]) / 2;
    }

    ll sum = 0;
    for (int i = 2; i < n; ++i) {
        sum += a[i] * (n - i);
    }
    a[1] = (f[n] - sum) / (n - 1);
    
    sum = 0;
    for (int i = 2; i < n; ++i) {
        sum += a[i] * (i - 1);
    }
    a[n] = (f[1] - sum) / (n - 1);

    for (int i = 1; i <= n; ++i) {
        cout << a[i] << " \n"[i == n];
    }
} 

