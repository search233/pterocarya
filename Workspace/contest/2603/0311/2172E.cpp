using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const double PI = acos(-1.0);

vector s(5, vector<string>());

void solve() {
    string ss; cin >> ss;
    int len = ss.length();
    int j, k; cin >> j >> k;
    int x = 0;
    string sj = s[len][j];
    string sk = s[len][k];
    // cout  << ss << ' ' << sj << ' ' << sk << ' ';
    for (int i = 0; i < len; ++i) {
        if (sj[i] == sk[i]) ++x;
    }

    cout << x << 'A' << len - x << "B\n";
} 

