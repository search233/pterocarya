const double PI = acos(-1.0);

void solve() {
    string x; cin >> x;

    int sum = 0;
    for (auto c : x) {
        sum += c - '0';
    }

    --x[0];
    ranges::sort(x);
    int ans = 0;
    for (int i = x.length() - 1; i >= 0; --i) {
        if (sum < 10) {
            break;
        }
        sum -= x[i] - '0';
        ++ans;
    }


    cout << ans << '\n';
} 

