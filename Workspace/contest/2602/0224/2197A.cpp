void solve() {

    int x; cin >> x;

    auto d = [](int num) -> int {
        int sum = 0;
        while (num) {
            sum += num % 10;
            num /= 10;
        }
        return sum;
    };
    
    int cnt = 0;
    for (int i = 0; i <= 100000; ++i) {
        if (d(x + i) == i) {
            ++cnt;
        }
    }

    cout << cnt << '\n';
} 

