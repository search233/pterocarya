#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);

vector<int> p;

void solve() {
    int n; cin >> n;

    vector<ll> a(n);
    for (auto& i : a) cin >> i;

    for (auto& i : p) {
        for (auto& num : a) {
            if (num % i) {
                cout << i << '\n';
                return;
            }
        }
    }
}

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    auto isprime = [&](int num) -> bool {
        for (int i=2 ; i<200 && i < num ; ++i) {
            if (num % i == 0) {
                return 0;
            }
        }

        return 1;
    };
 
    for (int i=2 ; i<55 ; ++i) {
        if (isprime(i)) p.push_back(i);
    }

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n";
    }

    return 0;
}
