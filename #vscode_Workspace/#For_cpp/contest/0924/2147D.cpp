#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<ll, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;

    map<ll, ll> mp;
    ll a = 0, b = 0;

    for (int i=0 ; i<n ; ++i) {
        ll num; cin >> num;
        if (num & 1) ++mp[num];
        else {
            a += num / 2;
            b += num / 2;
        }
    }

    vector<arr2> odd;

    for (auto& [num, cnt] : mp) {
        odd.pb({num, cnt});
    }

    auto cmp = [&](arr2 x, arr2 y) -> bool {
        return x[1] > y[1];
    };

    ranges::sort(odd, cmp);

    int tag = 1;
    for (auto& [num, cnt] : odd) {

        a += cnt * (num / 2);
        b += cnt * (num / 2);

        if (tag) a += cnt;
        else b += cnt;

        tag ^= 1;
    }

    cout << a << ' ' << b << '\n';
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
