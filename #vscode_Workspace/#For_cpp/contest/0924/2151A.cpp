#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, m;
    cin >> n >> m;

    int tag = 0;
    int cnt = 0;
    vector<int> a(m);
    map<int, int> mp;
    for (auto& i : a) {
        cin >> i;
        ++mp[i];
        if (i == 1) ++cnt;
        if (mp[i] > 1) tag = 1;
    }

    if (tag || (cnt && a[0] != 1)) {
        cout << 1 << '\n';
        return;
    }

    cout << n - a.back() + 1 << '\n';
}

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
