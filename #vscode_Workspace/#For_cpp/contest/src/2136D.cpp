const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;

    ll max1 = -LLONG_MAX;
    ll min2 = LLONG_MAX;

    for (int i=0 ; i<n ; ++i) {
        ll xi, yi;
        cin >> xi >> yi;

        max1 = max(max1, xi + yi);
        min2 = min(min2, xi - yi);
    }

    //num1 : 4k + x + y - min1
    //num2 : 4k + y - x + min2
    ll num1, num2;

    cout << "? U 1000000000" << endl;
    cin >> num1;

    cout << "? U 1000000000" << endl;
    cin >> num1;

    cout << "? R 1000000000" << endl;
    cin >> num1;

    cout << "? R 1000000000" << endl;
    cin >> num1;

    cout << "? L 1000000000" << endl;
    cin >> num2;

    cout << "? L 1000000000" << endl;
    cin >> num2;

    cout << "? L 1000000000" << endl;
    cin >> num2;

    cout << "? L 1000000000" << endl;
    cin >> num2;

    ll Y = (num1 + num2 - min2 + max1 - 8000000000) / 2;
    ll X = 4000000000 + Y + min2 - num2;

    cout << "! " << X << ' ' << Y << endl;
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n
    }

    return 0;
}
