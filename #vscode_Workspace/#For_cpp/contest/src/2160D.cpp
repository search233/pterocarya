#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;

    vector<int> ans(n * 2 + 1);

    vector<int> uknw;
    uknw.push_back(1);

    vector<int> knw;

    for (int i=2 ; i<= n * 2 ; ++i) {
        cout << "? " << uknw.size() + 1;
        for (auto& num : uknw) cout << ' ' << num;
        cout << ' ' << i <<  endl;

        int res; cin >> res;

        if (res) {
            knw.push_back(i);
            ans[i] = res;
        }
        else  uknw.push_back(i);
    }

    for (int i=0 ; i<n ; ++i) {

        cout << "? " << n + 1;
        for (auto& num : knw) cout << ' ' << num;
        cout << ' ' << uknw[i] <<  endl;

        int res; cin >> res;

        ans[uknw[i]] = res;
    }

    cout << '!';
    for (int i=1 ; i<=n * 2 ; ++i) {
        cout << ' ' << ans[i];
    }

    cout << endl;
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n
    }

    return 0;
}
