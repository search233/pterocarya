using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, k;
    cin >> n >> k;

    vector<vector<char>> a(n + 1, vector<char> (n + 1, 'D'));

    int left = n * n - k;

    if (left == 1) {
        cout << "NO\n";
        return;
    }

    cout << "YES\n";

    if (left <= n) {
        for (int i=1 ; i<left ; ++i) {
            a[1][i] = 'R';
        }
        a[1][left] = 'L';
    }
    else {
        for (int i=1 ; i<n ; ++i) {
            a[1][i] = 'R';
        }
        a[1][n] = 'L';

        left -= n;
        int x = 2, y = 1;

        while (left) {
            a[x][y] = 'U';

            ++y;

            if (y > n) {
                y = 1;
                ++x;
            }
            --left;
        }
    }

    for (int i=1 ; i<=n ; ++i) {
        for (int j=1 ; j<=n ; ++j) {
            cout << a[i][j];
        }
        cout << '\n';
    }
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
