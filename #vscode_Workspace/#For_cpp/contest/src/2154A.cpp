#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, k; 
    cin >> n >> k;

    string s; cin >> s;

    s = ' '  + s;

    int ans = 0;
    vector<int> pre(n + 1);

    for (int i=1 ; i<=n ; ++i) {

        pre[i] = pre[i - 1];

        if (s[i] == '1') {

            if (pre[i] == 0 || i - k >= pre[i]) {
                ++ans;
            }

            pre[i] = i;
        }       
    }

    cout << ans << '\n';
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n
    }

    return 0;
}
