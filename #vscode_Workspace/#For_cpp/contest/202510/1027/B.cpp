const double PI = acos(-1.0);


void solve() {
    string s;
    cin >> s;

    int cnt[2][3] = {0};

    for (int i=0 ; i<s.length() ; ++i) {
        ++cnt[i & 1][s[i] - '0'];
    }

    // cout << s << '\n';

    cnt[0][0] = abs(cnt[0][0] - cnt[1][0]);
    cnt[0][1] = abs(cnt[0][1] - cnt[1][1]);
    cnt[0][2] += cnt[1][2];

    int tmp = min(cnt[0][0], cnt[0][2]);

    cnt[0][0] -= tmp;
    cnt[0][2] -= tmp;

    tmp = min(cnt[0][1], cnt[0][2]);

    cnt[0][1] -= tmp;
    cnt[0][2] -= tmp;

    if (cnt[0][2]) {
        cout << cnt[0][2] % 2 << '\n';
    }
    else {
        cout << cnt[0][0] + cnt[0][1] << '\n';
    }

} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n
    }

    return 0;
}
