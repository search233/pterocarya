const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int a, b;
    cin >> a >> b;

    auto cal = [&](int num, int& ans) -> int {
        int cnt = 0;
        ans = 0;

        while (num) {
            if (!(num & 1)) 
                ans += (1ll << cnt);

            num >>= 1;
            ++cnt;
        }

        return cnt;
    };

    int ans1, ans2;

    if (cal(a, ans1) < cal(b, ans2)) {
        cout << "-1\n";
    }
    else {
        cout << "2\n";
        cout << ans1 << ' ';

        a ^= ans1;

        cout << (a ^ b) << '\n';
    }

} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n
    }

    return 0;
}
