using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    ll n; cin >> n;

    cout << "2 1 " << n << endl;
    ll tmp; cin >> tmp;

    ll dif = tmp - n * (n + 1) / 2;



    auto cal = [](int l, int r) -> ll {
        ll sum1;
        cout << "1 " << l << ' ' << r << endl;
        cin >> sum1;
        
        ll sum2;
        cout << "2 " << l << ' ' << r << endl;
        cin >> sum2;

        return sum2 - sum1;
    };


    int tl = 1, tr = n;

    while (tl <= tr) {
        int mid = (tl + tr) >> 1 ;

        if (cal(tl, mid) > 0) 
            tr = mid - 1;
        else 
            tl = mid + 1;
    }

    int l = tl;

    int r = l + dif - 1;

    cout << "! " << l << ' ' << r << endl;
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n
    }

    return 0;
}
