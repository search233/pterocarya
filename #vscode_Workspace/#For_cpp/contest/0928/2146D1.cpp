#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    ll l, r;
    cin >> l >> r;

    cout << (r + 1) * r << '\n';

    vector<ll> ans(r + 1);

    auto cal = [&](ll num) -> ll {
        ll fac = 1, res = 0;

        while (num) {
            if (!(num & 1)) {
                res += fac;
            }

            num >>= 1;
            fac <<= 1ll;
        }

        return res;
    };

    for (int i=r ; i>=0 ; --i) {
        // cout << i << ' ' << cal(i) << '\n';

        if (!ans[i]) {
            ll tmp = cal(i);
            ans[i] = tmp;
            ans[tmp] = i;
        }
    }

    for (int i=0 ; i<=r ; ++i) {
        cout << ans[i] << " \n"[i == r];
    }
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
