#include<stdio.h>

int main()
{
	double x, y;
	
	scanf("%lf", &x);
	
	if(x <= 100000){
		
		y = 0.1 * x;
		
	}else if(x > 100000 && x <= 200000){
		
		y = (x - 100000) * 0.075 + 10000;
		
	}else if(x > 200000 && x <= 400000){
		
		y = (x - 200000) * 0.05 + 17500;
		
	}else if(x > 400000 && x <= 600000){
		
		y = (x -400000) * 0.03 + 27500;
		
	}else if(x > 600000 && x <= 1000000){
			
		y = (x -600000) * 0.015 + 33500;
			
	}else {
			
		y = (x -1000000) * 0.01 + 39500;
			
	}
	
	printf("%.2f", y);
	
	return 0;
}
