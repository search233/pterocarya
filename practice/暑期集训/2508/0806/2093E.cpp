#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;


void solve() {
    int n, k; cin >> n >> k;

    vector<int> a(n);

    int cnt0 = 0;
    for (int i=0 ; i<n ; ++i) {
        cin >> a[i];
        if (a[i] == 0) ++cnt0;
    }

    if (cnt0 < k) {
        cout << 0 << '\n';
        return;
    }

    auto check = [=](int x) -> bool {
        int cnt = 0;
        set<int> s;

        for (int i=0 ; i<n ; ++i) {
            if (a[i] < x)
            s.insert(a[i]);

            if (s.size() == x) {
                s.clear();
                ++cnt;
            }
        }

        return cnt >= k;
    };

    int l=0, r=n+1;
    int mid, ans = 0;

    while (l <= r) {
        mid = (l + r) >> 1;

        if (check(mid)) {
            ans = max(ans, mid);
            l = mid + 1;
        } else r = mid - 1; 
    }

    cout << ans << '\n';
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
