#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;

    string s; cin >> s;
    s = ' ' + s;

    vector<int> sum(n * 2, 0);

    for (int i=1 ; i<=n * 2 - 1 ; ++i) {
        sum[i] = sum[i - 1] + (s[i] == 'W');
        // cout << sum[i] << '\n';
    }

    int res = 0;
    for (int i=1 ; i<=n ; ++i) {
        res = max(res, sum[i + n - 1] - sum[i - 1]);
    }

    cout << res <<'\n';
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    // cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
