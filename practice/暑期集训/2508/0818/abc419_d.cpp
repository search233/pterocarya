#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, m; cin >> n >> m;

    string s, t;
    cin >> s >> t;

    vector<int> tag(n + 1, 0);

    for (int i=0 ; i<m ; ++i) {
        int l, r;
        cin >> l >> r;

        ++tag[l - 1];
        --tag[r];
    }

    for (int i=0 ; i<n ; ++i) {
        if (i) tag[i] += tag[i - 1];
        // cout << tag[i] << ' ';
        if (tag[i] & 1) cout << t[i];
        else cout << s[i];
    }
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    // cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
