#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;

    vector<pii> a(n);

    for (int i=1 ; i<=n ; ++i) {
        cout << "? " << i << ' ' << n;
        for (int j=1 ; j<=n ; ++j) 
            cout << ' ' << j;

        cout << endl;

        int tmp; cin >> tmp;
        a[i - 1] = pii{i, tmp};
    }

    auto cmp = [](pii x, pii y) -> bool {return x.second > y.second; };

    sort(a.begin(), a.end(), cmp);

    vector<int> res;
    
    res.push_back(a[0].first);

    for (int i=1 ; i<a.size() ; ++i) {
        cout << "? " << res[0] << ' ' << res.size() + a.size()-i-1;

        for (auto& x : res) cout << ' ' << x;

        for (int j=i + 1 ; j<a.size() ; ++j)
            cout << ' ' << a[j].first;
        
        cout << endl;

        int tmp; cin >> tmp;

        if (tmp != a[0].second) {
            res.push_back(a[i].first);
        } 
    }

    cout << "! " << a[0].second;
    for (auto& i : res) cout << ' ' << i;

    cout << endl;
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
