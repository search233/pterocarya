#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, m; cin >> n >> m;

    vector<int> a(n);
    vector<int> b(m);

    for (auto& i : a) cin >> i;
    for (auto& i : b) cin >> i;

    ranges::sort(b);

    a[0] = min(b[0] - a[0], a[0]);

    for (int i = 1 ; i < n ; ++i) {
        int tag = lower_bound(b.begin(), b.end(), a[i] + a[i-1]) - b.begin();

        if (tag != m) {
            int tmp = b[tag] - a[i];

            if (a[i] > tmp || a[i] < a[i - 1]) 
                a[i] = b[tag] - a[i];
        } else {
            if (a[i] < a[i - 1]) {
                cout << "NO\n";
                return;
            }        
        }
    }

    cout << "YES\n";
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
