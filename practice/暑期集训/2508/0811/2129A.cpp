#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;
    
    vector<int> a(n + 1);
    vector<int> b(n + 1);

    for (int i=1 ; i<=n ; ++i)
        cin >> a[i] >> b[i];

    vector<int> res(n + 1, 1);
    for (int i=1 ; i<=n ; ++i) {
        for (int j=1 ; j<=n ; ++j) {
            if (j != i) {
                if (a[i] >= a[j] && b[i] <= b[j]) {
                    res[i] = 0;
                    // cout << "i = " << i << " is fking here!!" << '\n';
                    break;
                }
                    
            }
        }
    }


    int cnt = 0;
    for (int i=1 ; i<=n ; ++i) 
        if (res[i]) ++cnt;

    cout << cnt << '\n';
    for (int i=1 ; i<=n ; ++i) 
        if (res[i])
            cout << i << ' ';

    cout << '\n';
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
