#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)1e7 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;

bool vis[N];
int prime[N];
int minp[N];
int res[N];

void solve() {
    int n; cin >> n;

    cout << res[n] << '\n';
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    int cnt = 0;
    minp[1] = 1;

    for (int i=2 ; i<N ; ++i) {
        if (vis[i] == 0) {
            prime[++cnt] = i;
            minp[i] = i;
        }

        for (int j=1 ; j<=cnt && i * prime[j] <= N ; ++j) {
            vis[i * prime[j]] = 1;
            minp[i * prime[j]] = prime[j];
            if (i % prime[j] == 0) break;
        }
    }

    for (int i=2 ; i<=1e7 ; ++i) {
        int num = 0;
        int tmp = i;

        while (tmp != 1) {
            ++num;
            int fac = minp[tmp];
            while (tmp % fac == 0)
                tmp /= fac;
        }


        res[i] = res[i - 1] + num;
    }

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
