#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, k;
    cin >> n >> k;

    vector<int> a(n + 1);
    vector<int> b(n + 1);
    vector<pll> mp;
    ll res = 0;

    for (int i=1 ; i<=n ; ++i) cin >> a[i];
    for (int i=1 ; i<=n ; ++i) {
        cin >> b[i];
        res += abs(a[i] - b[i]);

        if (a[i] < b[i]) 
            swap(a[i], b[i]);

        mp.push_back(pll{a[i], b[i]});

        
    }

    auto cmp = [=](pll a, pll b) -> bool {
        return a.first > b.first;
    };

    sort(mp.begin(), mp.end(), cmp);

    ll minn = INT_MAX;

    for (int i=0 ; i<mp.size()-1 ; ++i) {
        if (mp[i].second <= mp[i + 1].first) {
            cout << res << '\n';
            return;
        }

        minn = min(minn, (mp[i].second - mp[i + 1].first) * 2);
    }
 
    cout << res + minn << '\n';

    // cout << res << '\n';
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
