#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    ll n, x, k;
    cin >> n >> x >> k;

    string s; cin >> s;

    ll res = 0;

    for (int i=0 ; i<n ; ++i) {
        x += s[i] == 'L' ? -1 : 1;

        --k;

        if (x == 0) {
            ++res;
            break;
        }

        if (i == n-1) {
            cout << res << '\n';
            return;
        }
    }

    int cnt = 0, tag = -1;
    for (int i=0 ; i<n ; ++i) {
        cnt += s[i] == 'L' ? -1 : 1;
        if (cnt == 0) {
            tag = i + 1;
            break;
        }
    }


    if (tag != -1) res += k / tag;

    cout << res << '\n';
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
