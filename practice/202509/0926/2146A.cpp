#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<ll, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;

    vector<arr2> a;

    for (int i=0 ; i<n ; ++i) {
        int tmp; 
        cin >> tmp;

        if (a.empty() || tmp != a.back()[0]) a.pb({tmp, 1});
        else ++a.back()[1];
    }

    auto cmp = [&](arr2 x, arr2 y) -> bool { return x[1] > y[1]; };

    ranges::sort(a, cmp);

    ll ans = 0;
    for (int i=0 ; i<a.size() ; ++i) {
        ans = max(ans, a[i][1] * (i + 1));
    }

    cout << ans << '\n';
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
