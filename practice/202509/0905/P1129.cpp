#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, tmp = 0; cin >> n;
    vector<int> match(n + 2, 0), vis(n + 2, 0);
    vector<vector<int>> graph(n + 5);

    for (int i=0 ; i<n ; ++i) {
        for (int j=0 ; j<n ; ++j) {
            int tmp; cin >> tmp;
            if (tmp) graph[i + 1].push_back(j + 1);
        }
    }

    auto dfs = [&](auto& dfs, int u) -> int {
        for (int v : graph[u]) {

            if (vis[v] == tmp) continue;
            vis[v] = tmp;

            if (!match[v] || dfs(dfs, match[v])) {
                match[v] = u;
                return 1;
            }
        }

        return 0;
    };

    int cnt = 0;
    for (int i=0 ; i<n ; ++i) {
        ++tmp;
        cnt += dfs(dfs, i + 1);
    }

    if (cnt == n) cout << "Yes\n";
    else cout << "No\n";
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
