#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, m;
    cin >> n >> m;

    vector<int> cnt(m + 1);

    vector<vector<int>> s(n);

    for (int i=0 ; i<n ; ++i) {
        int tmp; cin >> tmp;

        for (int j=0 ; j<tmp ; ++j) {
            int num; cin >> num;
            s[i].push_back(num);
            ++cnt[num];
        }
    }

    for (int j=1 ; j<=m ; ++j) {
        if (!cnt[j]) {
            cout << "No\n";
            return;
        }
    }

    int sum = 0;
    for (int i=0 ; i<n ; ++i) {
        for (int j=0 ; j<s[i].size() ; ++j) {
            if (cnt[s[i][j]] == 1) {
                break;
            }

            if (j == s[i].size() - 1) ++sum;
        }
    }

    if (sum < 2) cout << "No\n";
    else cout << "Yes\n";
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
