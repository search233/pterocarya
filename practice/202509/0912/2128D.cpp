#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;
    vector<int> a(n);
    
    for (auto& i : a) cin >> i;

    vector<int> dp, len(n);
    dp.push_back(a[0]);

    for (int i=0 ; i<n ; ++i) {
        if (dp.back() > a[i]) {
            dp.push_back(a[i]);
        }
        else {
            *lower_bound(dp.begin(), dp.end(), a[i], greater<int>()) = a[i];
        }

        len[i] = dp.size();
    }

    // for (int i=0 ; i<n ; ++i) cout << len[i] << ' ';
    // cout << '\n';

    ll ans = 1, num = 1;
    for (int i=1 ; i<n ; ++i) {
        ++num;
        if (len[i] > len[i - 1])
            num += i;
        
        ans += num;
        // cout << num << '\n';
    }

    cout << ans << '\n';
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
