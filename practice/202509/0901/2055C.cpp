#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, m;
    cin >> n >> m;

    string s; cin >> s;
    // if (s.back() == 'D') s += 'R';
    // else s += 'D';

    s += s.back();
    vector<vector<ll>> g(n + 1, vector<ll> (m + 1));

    for (int i=1 ; i<=n ; ++i) {
        for (int j=1 ; j<=m ; ++j) {
            cin >> g[i][j];
            // cout << 1 << ' ';
        }
        // cout << '\n';
    }

    // cout << n << ' ' << m << '\n';
    // return;

    int x = 1, y = 1;
    for (auto i : s) {
        ll sum = 0;

        if (i == 'D') {
            for (int j=1 ; j<=m ; ++j) {
                sum += g[x][j];
            }
            g[x][y] = -sum;
            ++x;
        } else {
            for (int i=1 ; i<=n ; ++i) {
                sum += g[i][y];
            }
            g[x][y] = -sum;
            ++y;
        }
    }

    for (int i=1 ; i<=n ; ++i) {
        for (int j=1 ; j<=m ; ++j) {
            cout << g[i][j] << ' ';
        }
        cout << '\n';
    }
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
