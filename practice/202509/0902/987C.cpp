#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);

void solve() {
    int n; cin >> n;
    vector<ll> s(n);
    vector<vector<ll>> c(3, vector<ll>(n, 0));

    for (auto& i : s) cin >> i;
    for (auto& i : c[0]) cin >> i;

    for (int i=0 ; i<n ; ++i) {
        ll mn = INF;
        for (int j=i ; j>=0 ; --j) {
            if (s[j] < s[i]) mn = min(mn, c[0][j]);
        }

        if (mn == INF) c[1][i] = -1;
        else c[1][i] = c[0][i] + mn;
    }

    ll res = INF;
    for (int i=0 ; i<n ; ++i) {
        ll mn = INF;
        for (int j=i ; j>=0 ; --j) {
            if (s[j] < s[i] && c[1][j] != -1) mn = min(mn, c[1][j]);
        }

        if (mn == INF) c[2][i] = -1;
        else c[2][i] = c[0][i] + mn;

        if (c[2][i] > 0) res = min(res ,c[2][i]);
    }

    if (res == INF) cout << -1;
    else cout << res;
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    // cin >> _T;

    while (_T--) {
        solve();
        // cout << "\n--------------\n";
    }

    return 0;
}
