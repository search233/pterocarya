#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;
    vector<ll> a(n);

    ll res = 0;
    for (int i=0 ; i<n ; ++i) {
        cin >> a[i];
        if (i & 1) res -= a[i];
        else res += a[i];
    } 

    ll maxn = n - 1;
    maxn -= !(n & 1);

// cout << maxn << '\n';

    int mx_o = 1;
    int mn_e = 0;
    for (int i=1 ; i<n ; ++i) {
        if (i & 1) {
            maxn = max(maxn, (a[i] - a[mn_e]) * 2 + i - mn_e);
            if (a[i] * 2 > a[mx_o] * 2 + i - mx_o) mx_o = i;
            // cout << maxn << ' ' << mn_e << ' ' << i << ' ' << mx_o << '\n';
        }
        else {
            maxn = max(maxn, (a[mx_o] - a[i]) * 2 + i - mx_o);
            if (-2 * a[i] > -2 * a[mn_e] + i - mn_e) mn_e = i;
            // cout << maxn << ' ' << mx_o << ' ' << i << ' ' << mn_e << '\n';
        }
        
    }

    cout << maxn + res << '\n';
}

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
