#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;
    vector<int> a(n + 1);
    map<int, bool> mp;
    stack<int> st;

    for (int i=1 ; i<=n ; ++i) {
        cin >> a[i];
        mp[a[i]] = 1;
    }

    for (int i=1 ; i<=n ; ++i) {
        if (mp[i] == 0) st.push(i);
    }

    for (int i=1 ; i<=n ; ++i) {
        if (a[i] == 0) {
            a[i] = st.top();
            st.pop();
        }
    }

    int l = 0, mx = 0;

    for (int i=1 ; i<=n ; ++i) {
        if (a[i] > i && l == 0) {
            l = i;
        }

        if (a[i] != i) mx = max(mx, a[i]);
    }

    if (l == 0) cout << 0 << '\n';
    else cout << mx - l + 1 << '\n';
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
