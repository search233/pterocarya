#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;
    vector<int> a(n);

    for (auto& i: a) cin >> i;


    int len = 0;

    vector<int> dp;
    dp.push_back(a[0]);

    for (int i=1 ; i<n ; ++i) {
        if (dp.back() < a[i]) {
            dp.push_back(a[i]);
        }
        else {
            *lower_bound(dp.begin(), dp.end(), a[i]) = a[i];
        }
    }

    cout << dp.size();
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    // cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
