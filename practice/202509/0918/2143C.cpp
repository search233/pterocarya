#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);

void solve() {
    int n; cin >> n;

    vector<int> cnt(n + 3, 0);
    vector<vector<int>> e(n + 3);

    for (int i=1 ; i<n ; ++i) {
        int u, v, x, y;
        cin >> u >> v >> x >> y;

        if (x > y) e[u].push_back(v), ++cnt[v];
        else e[v].push_back(u), ++cnt[u];

    }

    vector<int> ans(n + 1, 0);
    
    
    queue<int> qu;
    for (int i=1 ; i<=n ; ++i) {
        if (cnt[i] == 0) {
            qu.push(i);
        }
    }

    int num = n;

    while (!qu.empty()) {
        int u = qu.front();
        qu.pop();

        ans[u] = num;
        --num;

        for (auto v : e[u]) {
            --cnt[v];
            if (cnt[v] == 0) {
                qu.push(v);
            }
        }
    }

    for (int i=1 ; i<=n ; ++i) {
        cout << ans[i] << " \n"[i == n];
    }
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
