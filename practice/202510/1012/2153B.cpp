#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int x, y, z;
    cin >> x >> y >> z;

    vector<int> cnt;

    auto cal = [&](int num) -> void {
        int pos = 1;
        while (num) {
            if (pos > cnt.size()) {
                if (num & 1) cnt.pb(1);
                else cnt.pb(0);
            }
            else if (num & 1) ++cnt[pos - 1];

            num >>= 1;
            ++pos;
        }
    };

    cal(x), cal(y), cal(z);

    for (int i=0 ; i<cnt.size() ; ++i) {
        if (cnt[i] == 2) {
            cout << "NO\n";
            return;
        }
    }

    cout << "YES\n";
}

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n
    }

    return 0;
}
