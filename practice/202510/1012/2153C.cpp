#include <bits/stdc++.h>
#define int long long

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;
    
    map<int, int> mp;

    for (int i=0 ; i<n ; ++i) {
        int tmp; cin >> tmp;
        ++mp[tmp];
    }

    vector<int> a;

    int sum = 0, res = 0;
    for (auto& [num, cnt] : mp) {

        if (cnt > 1) {
            res += 2 * num * (cnt / 2);
            sum += cnt / 2 * 2;
        }

        if (cnt & 1) a.push_back(num);
    } 

    if (!sum) {
        cout << "0\n";
        return;
    }

    int plus = 0;

    for (int i=a.size() - 1 ; i>=0 ; --i) {
        if (a[i] < res) {
            ++sum;
            plus = a[i];
            break;
        }
    }

    for (int r = a.size() - 1 ; r > 0 ; --r) {
        if (a[r] - a[r - 1] < res) {
            ++sum;
            plus = max(plus, a[r] + a[r - 1]);
            break;
        }
    } 

    if (sum < 3) cout << "0\n";
    else cout << res + plus << '\n'; 
} 
 
signed main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n
    }

    return 0;
}
