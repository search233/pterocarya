#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;

    deque<ll> a;

    for (int i=0 ; i<n ; ++i) {
        int tmp; cin >> tmp;
        a.push_back(tmp);
    }

    auto cal = [&](vector<ll> && vec) -> ll {
        ranges::sort(vec);
        return vec.back() - vec.front();
    };

    ll ans = LLONG_MAX;
    for (int i=0 ; i<3 ; ++i) {
        
        vector<ll> dp(n + 1);
        dp[2] = cal(vector<ll> {a[0], a[1]});
        dp[3] = cal(vector<ll> {a[0], a[1], a[2]});

        for (int j=4 ; j<=n ; ++j) {

            dp[j] = dp[j - 2] + cal( vector<ll> {a[j - 1], a[j - 2]} );

            if (j > 4) dp[j] = min(dp[j], dp[j - 3] + cal( vector<ll> {a[j - 1], a[j - 2], a[j - 3]} ) );

            // cout << dp[j] << '\n';
        }

        ans = min(ans, dp[n]);

        a.push_back(a.front());
        a.pop_front();
    }

    cout << ans << '\n';
} 

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n";
    }

    return 0;
}
