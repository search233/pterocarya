#include <bits/stdc++.h>
#define pb push_back

using namespace std;
using ll = long long;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, q; cin >> n >> q;

    string s; cin >> s;

    ll a = 0, b = 1;

    for (int i=0 ; i<n ; ++i) {
        if (s[i] == 'A') {
            a += b;
        }
        else {
            b <<= 1ll;
        }
    }

    

    for (int i=0 ; i<q ; ++i) {
        int ans = 0;
        int num; cin >> num;

        if (b == 1) {
            cout << num << '\n';
            continue;
        }

        while (num >= b + a) {
            ans += n;

            num -= a;
            num /= b;
        }

        for (int j=0 ; num ; ++j) {
            if (s[j] == 'A') {
                --num;
            }
            else {
                num /= 2;
            }

            ++ans;
        }

        cout << ans << '\n';
    }
  
}

int main() {
    
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n
    }

    return 0;
}
