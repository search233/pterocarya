void solve() {
    string s; cin >> s;
    stack<string> st;

    for (int i = 0; i < s.length(); ++i) {
        if (s[i] == '(') {
            st.push("(");
        }
        else if (s[i] == ')') {
            while (st.top()[0] != '(') {
                cout << st.top() << '\n';
                st.pop();
            }
            st.pop();
        }
        else {
            if (st.top()[0] == '(') {
                string ss = "";
                st.push(ss);
            }
            st.top().push_back(s[i]);
        }
    }
} 

