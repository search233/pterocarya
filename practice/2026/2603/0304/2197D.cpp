void solve() {
    int n; cin >> n;
    vector<ll> a(n + 1);

    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }

    ll cnt = 0;
    int b = sqrt(n);
    for (ll j = 1; j <= n; ++j) {
        for (ll ai = 1; ai <= b; ++ai) {
            ll i = j - ai * a[j];
            if (i > 0 && i < j && a[i] == ai) {
                ++cnt;
            }
        }
    }

    for (ll i = 1; i <= n; ++i) {
        if (a[i] <= b) continue;
        for (ll aj = 1; aj <= b; ++aj) {
            ll j = a[i] * aj + i;
            if (j <= n && j > i && aj == a[j]) {
                ++cnt;
            }
        }
    }

    cout << cnt << '\n';
} 

