void solve() {
    int n, k;
    cin >> n >> k;

    if (k < n || k > n + n - 1) {
        cout << "NO\n";
        return;
    }

    cout << "YES\n";
    k -= n;

    if (k == 0) {
        for (int i = 1; i <= n; ++i) {
            cout << i << ' ' << i << " \n"[i == n];
        }
        return;
    }
    
    --k;
    cout << 1 << ' ' << n << ' ';
    for (int i = 2; i <= n; ++i) {
        if (k) {
            cout << i << ' ' << i - 1 << " \n"[i == n];
            --k;
        }
        else {
            cout << i - 1 << ' ' << i << " \n"[i == n];
        }
    }
} 

