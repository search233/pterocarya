const double PI = acos(-1.0);

void solve() {
    int a, b; cin >> a >> b;
    int n; cin >> n;

    int g = gcd(a, b);
    vector<int> fac;

    int tmp = sqrt(g);
    for (int i = 1; i <= tmp; ++i) {
        if (g % i) continue;
        fac.push_back(i);
        fac.push_back(g / i);
    }

    ranges::sort(fac);
    for (int i = 1; i <= n; ++i) {
        int lw, hi; cin >> lw >> hi;
        int p = upper_bound(fac.begin(), fac.end(), hi) - fac.begin() - 1;
        if (p < 0 || fac[p] < lw) {
            cout << -1 << '\n';
        }
        else {
            cout << fac[p]  << '\n';
        }
    }
}

