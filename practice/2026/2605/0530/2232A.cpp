void solve() {
    int n; cin >> n;
    vector<int> a(n + 1);

    map<int, int> mp;
    for (int i = 1; i <= n ;++i) {
        cin >> a[i];
        ++mp[a[i]];
    }


    vector<int> b(1, 0);
    for (auto [num, cnt] : mp) {
        b.push_back(cnt);
    }

    int m = b.size() - 1;


    vector<int> pre(m + 5);
    vector<int> suf(m + 5);

    for (int i = 1; i <= m; ++i) {
        pre[i] = pre[i - 1] + b[i];
    }

    for (int i = m; i > 0; --i) {
        suf[i] = suf[i + 1] + b[i];
    }

    int ans = n;
    for (int i = 1; i <= m; ++i) {
        ans = min(ans, max(pre[i - 1], suf[i + 1]));
    }

    cout << ans << '\n';
} 

