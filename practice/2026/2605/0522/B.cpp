using namespace std;
using ll = long long;
using uint = uint32_t;
using ull = uint64_t;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const double PI = acos(-1.0);

void solve() {
    int n; cin >> n;
    vector<ll> a(n + 1);

    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }

    auto cal = [&](int tag) -> ll {
        return a[tag] * (n - tag + 1ll);
    };

    int tag = -1;
    ll mn = 1e18;
    ll sum = 0;
    for (int i = 1; i <= n; ++i) {
        if (mn > cal(i) + sum) {
            mn = cal(i) + sum;
            tag = i;
        }
        sum += a[i];
    }

    assert(tag != -1);
    cout << tag << '\n';
}

