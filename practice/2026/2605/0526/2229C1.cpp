const double PI = acos(-1.0);

void solve() {
    int n; cin >> n;
    vector<ll> a(n + 1);

    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }
    
    int f = 1;
    vector<int> ans;
    for (int i = n; i > 0; --i) {
        if ((a[i] > 0 && f) || (a[i] < 0 && !f)) {
            f ^= 1;
            ans.push_back(i);
        }
    }
    cout << ans.size() << '\n';
    for (auto i : ans) cout << i << ' ';
    cout << '\n';
}

