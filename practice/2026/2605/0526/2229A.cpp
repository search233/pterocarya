void solve() {
    int n; cin >> n;
    vector<int> a(n + 1);

    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }

    int mn = 1e9;
    for (int i = 0; i <= 1000; ++i) {
        int ans = 0;
        for (int j = 1; j <= n; ++j) {
            ans = max(ans, abs(i - a[j]));
        }
        mn = min(mn, ans);
    }

    cout << mn << '\n';
}

