using uint = uint32_t;
using ull = uint64_t;
using arr2 = array<ll, 2>;
using arr3 = array<int, 3>;
const double PI = acos(-1.0);

void solve() {
    ll n; cin >> n;
    arr2 ans = {};
    int f = 0;
    int cnt = 0;

    while (n) {
        if (n & 1) {
            ans[f] += 1;
            n -= 1;
        }
        else if (n == 4) {
            ans[f] += 3;
            ans[f ^ 1] += 1;
            break;
        }
        else if (n % 4 == 0) {
            ans[f] += 1;
            n -= 1;
        }
        else {
            ans[f] += (n >> 1);
            n >>= 1;
        }

        f ^= 1;
    }

    cout << ans[0] << '\n';
}

