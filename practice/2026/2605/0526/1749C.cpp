const double PI = acos(-1.0);

void solve() {
    int n; cin >> n;
    deque<int> a(n);

    for (int i = 0; i < n; ++i) {
        cin >> a[i];
    }

    sort(a.begin(), a.end());

    auto ck = [&](int k) -> bool {
        deque<int> b(a);

        for (int i = k; i >= 1; --i) {
            if (b.empty()) return 0;

            while (!b.empty() && b.back() > i) {
                b.pop_back();
            }

            if (b.empty()) return 0;

            b.pop_back();
            if (!b.empty()) b.pop_front();
        }
        return 1;
    };

    int l = 0, r = n;
    int mid;
    while (l <= r) {
        mid = (l + r) >> 1;
        if (ck(mid)) {
            l = mid + 1;
        }
        else {
            r = mid - 1;
        }
    }
    
    cout << r << '\n';
}

