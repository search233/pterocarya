void solve() {
    int n; cin >> n;
    vector a(n, vector<int>());

    int mxk = 0;
    for (int i = 0; i < n; ++i) {
        int k; cin >> k;
        mxk = max(mxk, k);
        for (int j = 0; j < k; ++j) {
            int num; cin >> num;
            a[i].push_back(num);
        }
    }

    int p = -1;
    vector<int> res;
    while (p + 1 < mxk) {
        ranges::sort(a);
        for (int i = 0; i < a[0].size(); ++i) {
            res.push_back(a[0][i]);
            ++p;
        }

        vector<vector<int>> tmp1;
        for (int i = 0; i < a.size(); ++i) {
            vector<int> tmp2;
            for (int j = a[0].size(); j < a[i].size(); ++j) {
                tmp2.push_back(a[i][j]);
            }

            if (!tmp2.empty()) {
                tmp1.push_back(tmp2);
            }
        }

        a = tmp1;
    }

    for (int i = 0; i < mxk; ++i) {
        cout << res[i] << " \n"[i == mxk - 1];
    }
}

