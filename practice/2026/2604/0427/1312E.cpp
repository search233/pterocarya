void solve() {
    int n; cin >> n;
    vector<int> a(n + 5);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
    }

    vector dp(n + 5, vector<int>(n + 5, 0));
    for (int l = n; l > 0; --l) {
        dp[l][l] = a[l];
        for (int r = l + 1; r <= n; ++r) {
            //dp[l][mid] - dp[mid + 1][r]   
            // cout <<  l << ' ' << r << '\n';
            for (int mid = l; mid < r; ++mid) {
                if (dp[l][mid] && dp[l][mid] == dp[mid + 1][r]) {
                    dp[l][r] = dp[l][mid] + 1;
                    // cout << l << ' ' << r << ' ' << dp[l][r] << '\n';
                }
            }
        }
    }

    vector<int> ans(n + 1);
    for (int r = 1; r <= n; ++r) {
        ans[r] = 1e6;
        for (int l = 1; l <= r; ++l) {
            // 1, 2 .. l .. r .. n-1, n
            if (dp[l][r]) {
                ans[r] = min(ans[r], ans[l - 1] + 1);
            }
        }
    }

    cout << ans[n] << '\n';
}

