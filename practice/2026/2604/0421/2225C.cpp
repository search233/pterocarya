void solve() {
    int n; cin >> n;
    vector<string> s(2);

    cin >> s[0] >> s[1];
    s[0] = ' ' + s[0];
    s[1] = ' ' + s[1];

    int ans = 0;
    vector<int> tag(n + 2);
    for (int i = 1; i <= n; ++i) {
        tag[i] = (s[0][i] != s[1][i]);
        ans += tag[i];
    }
    for (int i = 1; i < n; ++i) {
        if (tag[i] && tag[i + 1]) {
            if (s[0][i] == s[0][i + 1]) {
                ans -= 2;
                ++i;
            }
        }
    }

    cout << ans << '\n';
}

