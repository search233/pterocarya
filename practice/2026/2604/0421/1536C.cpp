void solve() {
    int n; cin >> n;
    string s; cin >> s;
    s = ' ' + s;

    int cnt[2] = {};
    map<arr2, int> mp;
    for (int i = 1; i <= n; ++i) {
        if (s[i] == 'D') ++cnt[0];
        else ++cnt[1];

        int g = gcd(cnt[0], cnt[1]);
        arr2 tmp = {cnt[0] / g, cnt[1] / g} ;
        ++mp[tmp];
        cout << mp[tmp] << " \n"[i == n];
    }
}

