using arr3 = array<int, 3>;
const double PI = acos(-1.0);

void solve() {
    ll n, s, x;
    cin >> n >> s >> x;

    vector<ll> a(n + 2), sum(n + 2);
    for (int i = 1; i <= n; ++i) {
        cin >> a[i];
        sum[i] += sum[i - 1] + a[i];
    }

    auto cal = [&](int x) -> ll {
        ll ans = 0;
        map<ll, int> mp;
        mp[0] = 1;
        for (int i = 1; i <= n; ++i) {
            if (a[i] > x) {
                mp.clear();
            }
            if (mp.count(sum[i] - s)) {
                ans += mp[sum[i] - s];
            }
            ++mp[sum[i]];
        }
        return ans;
    };

    cout << cal(x) - cal(x - 1) << '\n';
}

