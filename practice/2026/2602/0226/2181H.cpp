void solve() {
    ll w, h, d, n;
    cin >> w >> h >> d >> n;

    i128 fac = 1;
    fac *= w;
    fac *= h;
    fac *= d;


    if (fac % n) {
        cout << "-1\n";
    }
    else {
        n = fac / n;
        ll w1, h1, d1;
        w1 = gcd(w, n);
        n /= w1;
        h1 = gcd(h, n);
        n /= h1;
        d1 = gcd(d, n);
        
        w1 = w / w1 - 1;
        h1 = h / h1 - 1;
        d1 = d / d1 - 1;

        cout << w1 << ' '
             << h1 << ' '
             << d1;
    }
} 

