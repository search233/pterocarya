i128 div_ceil(i128 a, i128 b) {
    if (b < 0) a = -a, b = -b;
    if (a >= 0) return (a + b - 1) / b;
    else return a / b; // Ťīüśēįśó∂ C++ ŚźĎ 0 śą™śĖ≠ÔľĆśú¨ŤļęŚįĪśėĮ ceil
}

// ŚźĎšłčŚŹĖśēīÁöĄťô§ś≥ēÔľöfloor(a / b)ÔľĆb > 0
i128 div_floor(i128 a, i128 b) {
    if (b < 0) a = -a, b = -b;
    if (a >= 0) return a / b;
    else return (a - (b - 1)) / b;
}

