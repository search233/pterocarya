ostream& operator<<(ostream& os, i128 x) {
    if (x == 0) {
        os << '0';
        return os;
    }
    if (x < 0) {
        os << '-';
        x = -x;
    }
    string s;
    while (x > 0) {
        int digit = (int)(x % 10);
        s.push_back('0' + digit);
        x /= 10;
    }
    reverse(s.begin(), s.end());
    os << s;
    return os;
}

