void solve() {
    int n, m;
    cin >> n >> m;

    priority_queue<int> a, b;
    for (int i = 0; i < n; ++i) {
        int num; cin >> num;
        a.push(num);
    }

    for (int i = 0; i < m; ++i) {
        int num; cin >> num;
        b.push(num);
    }

    int tag = 1;

    // while (!a.empty()) {
    //     cout << a.top() << ' ';
    //     a.pop();
    // }
    while (!a.empty() && !b.empty()) {       

        if (tag) {
            if (b.top() <= a.top()) {
                b.pop();
            }
            else {
                b.push(b.top() - a.top());
                b.pop();
            }
        }
        else {
            if (a.top() <= b.top()) {
                a.pop();
            }
            else {
                a.push(a.top() - b.top());
                a.pop();
            }
        }

        tag ^= 1;
    }

    if (a.empty()) {
        cout << "Bob\n";
    }
    else {
        cout << "Alice\n";
    }
} 

