#include <bits/stdc++.h>
#define __BUFF__ ios::sync_with_stdio(false);cin.tie(0);

using namespace std;
using ll = long long;
using uint = uint32_t;
using ull = uint64_t;
using arr2 = array<int, 2>;
using arr3 = array<int, 3>;
const double PI = acos(-1.0);
const ll mod = 998244353;

ll jc[300010];

void solve() {
    int n; cin >> n;
    vector<int> a(n), b(n);

    for (auto& i : a) cin >> i;
    for (auto& i : b) cin >> i;

    int mn = n * 2;
    for (int i = 0; i < n; ++i) {
        mn = min(mn, b[i]);
    }

    int cnt = 0;
    for (int i = 0; i < n; ++i) {
        if (a[i] < mn) ++cnt;
    }

    // cout << cnt << '\n';
    cout << jc[cnt] * jc[n - cnt] % mod << '\n';
} 

int main() {
    
    __BUFF__

    jc[0] = 1;
    for (ll i = 1; i <= 300000; ++i) {
        jc[i] = jc[i - 1] * i % mod;
    }

    int _ = 1;
    cin >> _;

    while (_--) {
        solve();
        // cout << "-----------\n";
    }

    return 0;
}
