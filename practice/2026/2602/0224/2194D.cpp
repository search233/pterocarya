void solve() {
    int n, m;
    cin >> n >> m;

    vector a(n + 1, vector<int> (m + 1));

    ll cnt = 0;
    for (int i = 1; i <= n; ++i) {
        for (int j = 1; j <= m; ++j) {
            cin >> a[i][j];
            a[i][0] += a[i][j];
        }
        cnt += a[i][0];
    } 

    cout << (cnt / 2) * ((cnt + 1) / 2) << '\n';
    cnt /= 2;
    string s;
    int f = 0;
    for (int i = 1; i <= n; ++i) {

        if (f) {
            s.push_back('D');
            continue;
        }

        if (a[i][0] <= cnt) {
            cnt -= a[i][0];
            s.push_back('D');
        }
        else {
            int p = 1;
            cnt = a[i][0] - cnt;
            while (cnt) {
                cnt -= a[i][p];
                ++p;
                s.push_back('R');
            }
            s.push_back('D');
            while (p <= m) {
                s.push_back('R');
                ++p;
            }
            f = 1;
        }
    }

    while (s.length() < m + n) s.push_back('R');
    cout << s << '\n';
} 

