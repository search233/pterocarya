using arr3 = array<int, 3>;
const double PI = acos(-1.0);

void solve() {
    int n; cin >> n;

    vector<int> a(n);

    for (auto& i : a) cin >> i;

    for (int i = 1; i < n; ++i) {
        if (!((a[i] & 1) ^ (a[i - 1] & 1))) {
            cout << "NO\n";
            return;
        }
    }

    cout << "YES\n";
} 

