#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);

vector<int> C;

void solve() {
    int k; cin >> k;

    int sum = 0;
    int tmp = k;
    vector<int> res;
    for (int i=445 ; i>=0 ; --i) {
        while (tmp >= C[i]) {
            res.push_back(i + 2);
            tmp -= C[i];
            sum += i + 2;
        }

        if (tmp == 0) break;
    }

    int x = (int)-1e9;
    int y = (int)-1e9;

    cout << sum << '\n';
    for (auto& w : res) {
        for (int i=0 ; i<w ; ++i) {
            cout << x << ' ' << y << '\n';
            ++x;
        }
        ++y;
        // cout << w << '\n';
    }
}

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    for (int i=2 ; i<=447 ; ++i) {
        C.push_back((i * (i - 1)) >> 1);
    }
    

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
