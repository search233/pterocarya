#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    ll n, k; cin >> n >> k;

    if (k >= n) {
        cout << n * 3 << '\n';
        return;
    }

    deque<ll> num;
    ll tmp = n;
    ll fac = 1;
    ll sum = 0;

    while (tmp) {
        num.push_front(tmp % 3);
        sum += tmp % 3;
        fac *= 3;
        tmp /= 3;
    }

    if (sum > k) {
        cout << -1 << '\n';
        return;
    }

    ll x = (k - sum) / 2;
    
    while (x) {
        if (num.front() <= x) {
            int temp = num.front();
            x -= temp;
            num.pop_front();
            num[0] += temp * 3;
        } else {
            num[0] -= x;
            num[1] += x*3;
            x = 0;
        }
    }

    ll res = 0;
    fac = 1;
    ll cnt = 0;
    for (int i=num.size()-1 ; i>=0 ; --i) {
        res += num[i] * (fac * 3 + cnt * fac / 3);
        fac *= 3;
        ++cnt;
    }

    cout << res << '\n';
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
