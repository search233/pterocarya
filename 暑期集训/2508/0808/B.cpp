#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);

const ll U = (1ll << 30) - 1;

void solve() {
    ll n, A, B ,C;
    cin >> n >> A >> B >> C;

    // 0, 1, 2  --->  -3 -2 -1
    ll f[3] = {A & U, B & U, C & U};
    ll g, h;


    int tag = 0;
    for (int i=0 ; i<n ; ++i) {
        g = f[0] ^ ((f[0] * (1 << 16ll)) & U);
        h = g ^ (g >> 5ll);

        f[0] = f[1];
        f[1] = f[2];
        f[2] = h ^ ((h << 1ll) & U) ^ f[0] ^ f[1];

        if ((f[2] % (n - i)) != 0) tag ^= 1;

        // cout << f[2] << ' ';
        // cout << tag << ' ';
    }

    cout << tag; 

    for (int i=1 ; i<n ; ++i) {
        for (int j=0 ; j<3 ; ++j) {
            g = f[0] ^ ((f[0] * (1 << 16ll)) & U);
            h = g ^ (g >> 5ll);

            f[0] = f[1];
            f[1] = f[2];
            f[2] = h ^ ((h << 1ll) & U) ^ f[0] ^ f[1];        
        }


        ll d = f[2] % n + 1;
        ll l = min(f[0] % n, f[1] % n);
        ll r = max(f[0] % n, f[1] % n);

        if ((r - l + 1 - d) * d & 1) tag ^= 1;


        // cout  << ' ' << l << ' ' << r << ' ' << d;
        // cout << f[2] << ' ';
        // cout << '\n';
        cout << tag;
    }


    cout << "\n";
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------\n" ;
    }

    return 0;
}
