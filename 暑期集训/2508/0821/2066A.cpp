#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;


    vector<int> x(n);
    map<int, int> mp;
    for (auto& i : x) {
        cin >> i;
        mp[i] = 1;
    }

    for (int i=1 ; i<=n ; ++i) {
        if (!mp[i]) {
            cout << "? " << i << ' ' << (i == 1 ? 2 : 1) << endl;
            int rep; cin >> rep;
            if (rep) cout << "! B" << endl;
            else cout << "! A" << endl;

            return;
        }
    }

    int mntag = 0;
    int mxtag = 0;
    for (int i=0 ; i<n ; ++i) {
        if (x[mxtag] < x[i]) mxtag = i;
        if (x[mntag] > x[i]) mntag = i;
    }

    int rep1, rep2;
    cout << "? " << mntag + 1 << ' ' << mxtag + 1 << endl;
    cin >> rep1;

    cout << "? " << mxtag + 1 << ' ' << mntag + 1 << endl;
    cin >> rep2;

    if (rep1 == rep2 && rep1 >= n-1) {
        cout << "! B" << endl;
    } else {
        cout << "! A" << endl;
    }
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
