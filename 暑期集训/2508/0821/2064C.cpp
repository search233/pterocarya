#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;

    vector<ll> a(n);

    for (auto& i : a) cin >> i;

    vector<ll> sum1(n); // 正向， 考虑 》 0
    vector<ll> sum2(n);

    for (int i=0 ; i<n ; ++i) {
        
        if (i > 0 && a[i] >= 0) 
            sum1[i] += sum1[i - 1] + a[i];
            
        else if (i == 0 && a[i] >= 0) 
            sum1[i] = a[i];

        else if (i > 0) 
            sum1[i] = sum1[i - 1];
        
    }

    for (int i=n-1 ; i>=0 ; --i) {

        if (i < n-1 && a[i] <= 0) 
            sum2[i] += sum2[i + 1] - a[i];
        
        else if (i == n-1 && a[i] <= 0) 
            sum2[i] = - a[i];

        else if (i < n-1) 
            sum2[i] = sum2[i + 1];
    }

    ll res = 0;
    for (int i=0 ; i<n ; ++i) {
        res = max(res, sum1[i] + sum2[i]);
    }

    cout << res << '\n';

} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
