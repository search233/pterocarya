#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;
    vector<ll> a(n * 2);

    map<ll, bool> mp;
    for (auto& x : a) {
        cin >> x;
        mp[x] = 1;
    }

    ranges::sort(a);

    vector<ll> res;

    int l = 0;
    int r = n * 2 - 1;

    while (l < r) {
        res.push_back(a[r]);
        res.push_back(a[l]);
        ++l;
        --r;
    }

    int tag = 1;
    ll sum = 0;
    for (auto& x : res) {
        if (tag) sum += x;
        else sum -= x;

        tag ^= 1;
    }

    sum += res.back() * 2;
    res.push_back(sum);
    swap(res[n * 2], res[n * 2 - 1]);

    // cout << sum;
    for (auto& x : res)
        cout << x << ' ';
    cout << '\n';
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
