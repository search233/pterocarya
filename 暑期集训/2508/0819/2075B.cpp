#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n, k; 
    cin >> n >> k;

    vector<ll> a(n);
    for (auto& x : a) cin >> x;

    ll res = 0;
    if (k > 1) {
        ranges::sort(a);
        for (int i=n-1 ; i>=0 && k+1 ; --i) {
            --k;
            res += a[i];
        }
    } else {
        res = max(a.front(), a.back());
        ll tmp = min(a.front(), a.back());
        for (int i=1 ; i<n-1 ; ++i)
            tmp = max(tmp, a[i]);

        res += tmp;
    }

    cout << res << '\n';
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
