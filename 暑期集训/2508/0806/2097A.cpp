#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;


void solve() {
    int n; cin >> n;

    map<int, int> mp;

    for (int i=0 ; i<n ; ++i) {
        int tmp; cin >> tmp;
        ++mp[tmp];
    }

    int lnum = -1;
    int tag = 0;

    for (auto [num, cnt] : mp) {

        if (cnt >= 4) {
            cout << "Yes\n";
            return;
        }

        if (tag) {
            if (num > lnum + 1) tag = 0;
            else lnum = num;
        }

        if (cnt > 1) {

            if (tag) {
                cout << "Yes\n";
                return;
            } 
            
            lnum = num;
            tag = 1;
            continue;
        }            
    }

    cout << "No\n";
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}

