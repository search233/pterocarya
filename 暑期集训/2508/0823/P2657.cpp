const ll INF = LLONG_MAX;
const double PI = acos(-1.0);

int cal(string s) {

    int memo[20][2][10][2];
    memset(memo, -1, sizeof(memo));

    auto dfs = [&](auto& dfs, int pos, bool lim, int pnum, bool zero) -> int {
        if (pos == s.length()) return 1;

        int& res = memo[pos][lim][pnum][zero];
        if (~res) return res;
        else res = 0;

        int upbd = 9;
        if (lim) upbd = s[pos] - '0';

        for (int i=0 ; i<=upbd ; ++i) {
            if (!zero && abs(i - pnum) < 2) continue;

            res += dfs(dfs, pos + 1, lim && (i == upbd), i, (zero && !i));
            // cout << pos << ' ' << pnum <<  i << ' ' << res << '\n';
        }

        return res;
    };

    return dfs(dfs, 0, 1, 0, 1);
}


void solve() {
    ll a, b;
    cin >> a >> b;

    string sa = to_string(a - 1);
    string sb = to_string(b);

    cout << cal(sb) - cal(sa);
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    // cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
