#include <bits/stdc++.h>

using namespace std;
using ll = long long;
using pll = pair<ll, ll>;
using pii = pair<int, int>;
using pdd = pair<double, double>;
const int N = (int)2e5 + 9;
const int M = (int)1e5 + 9;
const int mod =  998244353;
const ll INF = LLONG_MAX;
const double PI = acos(-1.0);


void solve() {
    int n; cin >> n;
    vector<int> a(n + 1);
    map<int, int> mp; //num, pos;

    for (int i=1 ; i<=n ; ++i) {
        cin >> a[i];
        mp[a[i]] = i;
    }

    int l = n / 2;
    int r = l + 1;
    if (n & 1) ++r;

    int res = n / 2;

    while (l > 0) {
        // cout << l << ' ' << r << '\n';
        if (r - l == 1 && mp[l] < mp[r]) --res;
        else {
            if (mp[l] < mp[r] && mp[l] < mp[l + 1] && mp[r] > mp[r - 1]) --res;
            else break;
        }
        --l, ++r;
    }

    cout << res << '\n';
} 

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);

    int _T = 1;
    cin >> _T;

    while (_T--) {
        solve();
        // cout << "-----------" << '\n';
    }

    return 0;
}
